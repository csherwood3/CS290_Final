// These hover over functions were inspired by the code found on this website:
// https://exceptionshub.com/css-change-image-src-on-imghover.html. I take no
// credit for designing this code.

function hoverLogo(element) {
    element.setAttribute('src', 'assets/logo_small_hover.png');
}

function unhoverLogo(element) {
    element.setAttribute('src', 'assets/logo_small.png');
}

function hoverInsta(element) {
    element.setAttribute('src', 'assets/instagram_icon_hover.png');
}

function unhoverInsta(element) {
    element.setAttribute('src', 'assets/instagram_icon.png');
}

function hoverTwitter(element) {
    element.setAttribute('src', 'assets/twitter_icon_hover.png');
}

function unhoverTwitter(element) {
    element.setAttribute('src', 'assets/twitter_icon.png');
}

